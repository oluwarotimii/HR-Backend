/**
 * Script to create sample attachment files for testing
 * Creates placeholder PDF and image files in uploads/attachments directory
 * 
 * Usage: npm run create-sample-files
 */

import fs from 'fs';
import path from 'path';

const UPLOADS_DIR = path.join(process.cwd(), 'uploads', 'attachments');
const LEAVE_UPLOADS_DIR = path.join(process.cwd(), 'uploads', 'leave-requests');

// Create directories if they don't exist
[UPLOADS_DIR, LEAVE_UPLOADS_DIR].forEach(dir => {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
    console.log(`✓ Created directory: ${dir}`);
  }
});

